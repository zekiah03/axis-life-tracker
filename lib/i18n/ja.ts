// Loosened type: all leaf string literals become `string`, keeping functions and arrays structurally compatible.
type DeepLoosen<T> = T extends (...args: infer A) => infer R
  ? (...args: A) => string
  : T extends readonly string[]
  ? readonly string[]
  : T extends readonly (infer U)[]
  ? readonly DeepLoosen<U>[]
  : T extends object
  ? { [K in keyof T]: DeepLoosen<T[K]> }
  : T extends string
  ? string
  : T

export type TranslationShape = DeepLoosen<TranslationKeys>
